class Person {
  constructor(firstName, lastName, id, phone, street, city, state, zip) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.id = id;
    this.phone = phone;
    this.street = street;
    this.city = city;
    this.state = state;
    this.zip = zip;
  }
}

module.exports = Person;